  function botSays(text) {
    addMessage("Retailer (AI)", text);
    history.push({ role: "assistant", content: text });
  }
  function setTyping(on) {
    $("typing").style.visibility = on ? "visible" : "hidden";
  }
  const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

  function lastBotOffer() {
    const bot = offers.filter((o) => o.idx === BOT_ID);
    return bot.length ? bot[bot.length - 1] : null;
  }
  function refreshInterface() {
    const bot = lastBotOffer();
    $("otherProposal").innerHTML = bot
      ? `€ ${bot.price.toFixed(2)}<br>${bot.quantity}`
      : "(none)<br>&nbsp;";
    $("btnAccept").disabled = !bot || finished;
    const mine = offers.filter((o) => o.idx !== BOT_ID && !o.fromChat);
    $("myProposal").innerHTML = mine.length
      ? `€ ${mine[mine.length - 1].price.toFixed(2)}<br>${mine[mine.length - 1].quantity}`
      : "(none)<br>&nbsp;";
  }

  function finishDeal(price, quantity) {
    finished = true;
    refreshInterface();
    chatInput.disabled = true;
    $("btn-chat").disabled = true;
    $("btn-offer").disabled = true;
    $("btnAccept").disabled = true;
    const sp = profitSupplier(price, quantity, PRODUCTION_COST);
    $("deal-terms").textContent =
      `Deal: wholesale price ${price.toFixed(2)}€ · quantity ${quantity} units`;
    $("deal-profit").textContent =
      `Your expected profit as Supplier: ${sp.toFixed(2)} ` +
      `(wholesale revenue on expected sales minus production cost of 1.00€/unit).`;
    $("deal-panel").hidden = false;
    $("deal-panel").scrollIntoView({ behavior: "smooth", block: "center" });
  }

  async function respondTo(offer) {
    const evaluation = evaluate(offer);
    setTyping(true);
    await sleep(BOT_RESPONSE_DELAY);

    if (evaluation === EVAL.ACCEPT) {
      const terms = offerString(offer.price, offer.quantity);
      if (offer.fromChat) {
        const scripted = ACCEPT_FROM_CHAT.replace("%s", terms);
        const text = await llmPhrase(
          "ACCEPT the Supplier's chat offer of " + terms +
          "; tell them you have placed it in the interface as a binding " +
          "offer and they must click CONFIRM to finalize.",
          terms, scripted, history);
        setTyping(false);
        botSays(text);
        await sleep(BOT_ACCEPT_DELAY);
        offers.push(makeOffer(BOT_ID, offer.price, offer.quantity, false));
        refreshInterface();
      } else {
        const scripted = ACCEPT_FROM_INTERFACE.replace("%s", terms);
        const text = await llmPhrase(
          "ACCEPT the Supplier's binding offer of " + terms +
          " and announce you are finalizing the deal.",
          terms, scripted, history);
        setTyping(false);
        botSays(text);
        await sleep(BOT_ACCEPT_DELAY);
        finishDeal(offer.price, offer.quantity);
      }
      return;
    }

    const [cp, cq] = optimalCounterOffer(evaluation, offer);
    const scripted = scriptedResponse(evaluation, cp, cq);
    const terms = cp != null && cq != null ? offerString(cp, cq) : null;
    const intent = {
      [EVAL.NOT_PROFITABLE_ON_BOTH]:
        "REJECT their terms (both price and quantity unworkable) and counter with ",
      [EVAL.NOT_PROFITABLE_ON_PRICE]:
        "REJECT because the wholesale price is too high for you; counter with ",
      [EVAL.NOT_PROFITABLE_ON_QUANTITY]:
        "REJECT because that quantity does not work at that price; counter with ",
      [EVAL.OFFER_PRICE]:
        "They only named a price; propose completing it as ",
      [EVAL.OFFER_QUANTITY]:
        "They only named a quantity; propose completing it as ",
      [EVAL.NOT_OFFER]:
        "You could not read a complete offer from their message; ask for both " +
        "a wholesale price and a quantity, mentioning as reference ",
      [EVAL.INVALID_OFFER]:
        `Their terms are outside the allowed ranges (price ${PRICE_MIN}-${PRICE_MAX}€, ` +
        `quantity ${QTY_MIN}-${QTY_MAX}); ask them to adjust, mentioning as reference `,
    }[evaluation] + (terms || "different terms") +
      ". Mention that you have placed this offer in the interface.";

    const isReject = evaluation === EVAL.NOT_PROFITABLE_ON_BOTH ||
                     evaluation === EVAL.NOT_PROFITABLE_ON_PRICE ||
                     evaluation === EVAL.NOT_PROFITABLE_ON_QUANTITY;
    const text = await llmPhrase(intent, terms, scripted, history, isReject);
    setTyping(false);
    botSays(text);

    // Every set of terms the bot cites becomes its binding proposal in
    // the interface — including the reference terms it gives for
    // unreadable or out-of-range offers.
    if (cp != null && cq != null) {
      offers.push(makeOffer(BOT_ID, cp, cq, false));
      refreshInterface();
    }
  }

  /* ── Event handlers ────────────────────────────────────────────────── */
  window.sendMessage = async function () {
    if (busy || finished) return;
    const body = chatInput.value.trim();
    if (!body) return;
    busy = true;
    chatInput.value = "";
    addMessage("You (Supplier)", body);
    history.push({ role: "user", content: body });

    const offer = interpretOfferFromChat(body);

    // No readable terms at all → treat as conversation (questions,
    // preferences, small talk) and answer from the rule-derived facts.
    // Falls through to the scripted NOT_OFFER flow if the LLM is
    // unavailable or its reply fails validation.
    if (offer.price == null && offer.quantity == null) {
      setTyping(true);
      await sleep(BOT_RESPONSE_DELAY);
      const reply = await llmConverse(history);
      if (reply) {
        setTyping(false);
        botSays(reply.text);
        if (reply.citesNash) {
          // It offered its benchmark terms in chat → make them binding.
          const [np, nq] = reply.nash.offer;
          offers.push(makeOffer(BOT_ID, np, nq, false));
          refreshInterface();
        }
        busy = false;
        return;
      }
      setTyping(false);
    }

    await respondTo(offer);
    busy = false;
  };

  window.sendOffer = async function () {
    if (busy || finished) return;
    const price = parseFloat($("myPrice").value);
    const quantity = parseInt($("myQuantity").value, 10);
    if (isNaN(price) || isNaN(quantity)) {
      addMessage("System", "Enter both a price and a quantity first.");
      return;
    }
    // Hard bounds, as in the experiment: the interface refuses to send
    // terms outside the allowed ranges.
    if (price < PRICE_MIN || price > PRICE_MAX) {
      addMessage("System",
        `The wholesale price must be between ${PRICE_MIN} and ${PRICE_MAX}€.`);
      return;
    }
    if (quantity < QTY_MIN || quantity > QTY_MAX) {
      addMessage("System",
        `The quantity must be between ${QTY_MIN} and ${QTY_MAX} units.`);
      return;
    }
    busy = true;
    const offer = makeOffer(1, Math.round(price * 100) / 100, quantity, false);
    offers.push(offer);
    refreshInterface();
    addMessage("You (Supplier)",
      `[binding offer] ${offer.price.toFixed(2)}€ · ${offer.quantity} units`);
    history.push({ role: "user",
      content: `(binding interface offer) price ${offer.price}, quantity ${offer.quantity}` });
    await respondTo(offer);
    busy = false;
  };

  window.sendAccept = function () {
    if (busy || finished) return;
    const bot = lastBotOffer();
    if (!bot) return;
    addMessage("You (Supplier)",
      `[confirmed] ${bot.price.toFixed(2)}€ · ${bot.quantity} units`);
    finishDeal(bot.price, bot.quantity);
  };

  window.resetDemo = function () {
    offers = [];
    history.length = 0;
    finished = false;
    busy = false;
    chatOutput.innerHTML = "";
    chatInput.disabled = false;
    chatInput.value = "";
    $("btn-chat").disabled = false;
    $("btn-offer").disabled = false;
    $("myPrice").value = "";
    $("myQuantity").value = "";
    $("deal-panel").hidden = true;
    refreshInterface();
    greet();
  };

