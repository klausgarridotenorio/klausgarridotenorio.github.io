/* Automated Negotiation Demo
   ──────────────────────────
   Browser port of the oTree experiment's AI retailer
   (bot_negotiation.py / bot_strategy.py / offer.py / optimal.py /
   bot_messages.py). The DECISIONS (accept / counter, and with what terms)
   are rule-based and deterministic, exactly as in the experiment. The
   CHAT LINES are generated by a free LLM via OpenRouter when a key is
   provided; otherwise the experiment's scripted messages are used.

   Hybrid architecture, as in Garrido Tenorio, Becker-Peth & de Vries,
   "Hybrid (Rule-based + Gen AI) Chatbot: Automating Negotiations" (LNCS):
     rules decide → the LLM only phrases → outputs are validated
     post-generation before being shown.
*/
(function () {
  "use strict";

  /* ── Constants (constants.py) ─────────────────────────────────────── */
  const PRICE_MIN = 1, PRICE_MAX = 5;
  const QTY_MIN = 1, QTY_MAX = 100;
  const D_MIN = 0, D_MAX = 100;
  const PRODUCTION_COST = 1;      // common knowledge
  const ACTING_RP = 4;            // bot's no-disclosure retail price
  const BOT_RESPONSE_DELAY = 1500; // ms
  const BOT_ACCEPT_DELAY = 3000;   // ms
  const BOT_ID = -1;

  /* ── LLM settings (OpenRouter) ────────────────────────────────────── */
  // Chat generation: a free conversational NVIDIA model.
  const DEFAULT_CHAT_MODEL = "nvidia/nemotron-3-nano-omni-30b-a3b-reasoning:free";
  // Input moderation: NVIDIA's free content-safety guardrail model.
  const SAFETY_MODEL = "nvidia/nemotron-3.5-content-safety:free";
  const OPENROUTER_URL = "https://openrouter.ai/api/v1/chat/completions";
  const KEY_STORAGE = "neg-demo-openrouter-key";
  const MODEL_STORAGE = "neg-demo-chat-model";

  /* ── Expected demand / profits (offer.py) ─────────────────────────── */
  function expectedDemand(q) {
    if (q <= D_MIN) return q;
    if (q >= D_MAX) return (D_MIN + D_MAX) / 2;
    return ((q * q - D_MIN * D_MIN) / 2 + q * (D_MAX - q)) / (D_MAX - D_MIN);
  }
  function profitSupplier(w, q, c) { return w * expectedDemand(q) - c * q; }
  function profitRetailer(w, q, rp) { return (rp - w) * expectedDemand(q); }

  /* ── Nash bargaining solution (optimal.py) ────────────────────────── */
  function nashBargainingSolution(cA, cB) {
    const pm = Math.max(cA, cB), c = Math.min(cA, cB);
    const qCont = (D_MAX - D_MIN) * (pm - c) / pm;
    const priceStar = Math.round((pm * (pm + 3 * c) / (2 * (pm + c))) * 100) / 100;
    const candidates = [Math.floor(qCont), Math.ceil(qCont)];
    const total = (q) => profitSupplier(priceStar, q, c) + profitRetailer(priceStar, q, pm);
    const qStar = candidates.reduce((a, b) => (total(b) > total(a) ? b : a));
    const target = Math.floor(profitSupplier(priceStar, qStar, c) * 100) / 100;
    return { profit: target, offer: [priceStar, qStar] };
  }

  /* ── Offer + evaluation (offer.py) ────────────────────────────────── */
  const EVAL = {
    ACCEPT: "accept",
    NOT_PROFITABLE_ON_BOTH: "not_profitable_on_both",
    NOT_PROFITABLE_ON_PRICE: "not_profitable_on_price",
    NOT_PROFITABLE_ON_QUANTITY: "not_profitable_on_quantity",
    OFFER_QUANTITY: "offer_quantity",
    OFFER_PRICE: "offer_price",
    NOT_OFFER: "not_offer",
    INVALID_OFFER: "invalid_offer",
  };

  function makeOffer(idx, price, quantity, fromChat) {
    return { idx, price, quantity, fromChat: !!fromChat, stamp: Date.now() };
  }
  const priceInRange = (p) =>
    p != null && p >= PRICE_MIN && p <= PRICE_MAX && Math.round(p * 100) === p * 100;
  const qtyInRange = (q) => q != null && Number.isInteger(q) && q >= QTY_MIN && q <= QTY_MAX;
  const isValid = (o) => priceInRange(o.price) && qtyInRange(o.quantity);
  const isComplete = (o) => o.price != null && o.quantity != null;

  // The bot is the Retailer: constraint_user = production cost,
  // constraint_bot = acting retail price (bot_strategy.py).
  const CONSTRAINT_USER = PRODUCTION_COST;
  const CONSTRAINT_BOT = ACTING_RP;

  function botProfit(o) {
    return isValid(o) ? profitRetailer(o.price, o.quantity, CONSTRAINT_BOT) : null;
  }

  function isPriceFeasible(o, params) {
    if (o.price == null) return false;
    const esBest = expectedDemand(D_MAX);
    return (params.marketPrice - o.price) * esBest >= params.nashProfit;
  }
  function isQuantityFeasible(o, params) {
    if (o.quantity == null) return false;
    const es = expectedDemand(o.quantity);
    const maxAcceptablePrice = params.marketPrice - params.nashProfit / es;
    return maxAcceptablePrice >= params.productionCost;
  }

  function evaluate(offer) {
    const params = {
      nashProfit: nashBargainingSolution(CONSTRAINT_USER, CONSTRAINT_BOT).profit,
      productionCost: Math.min(CONSTRAINT_USER, CONSTRAINT_BOT),
      marketPrice: Math.max(CONSTRAINT_USER, CONSTRAINT_BOT),
    };
    const pb = botProfit(offer);
    if (pb != null && pb >= params.nashProfit) return EVAL.ACCEPT;

    if (isValid(offer)) {
      const priceBad = !isPriceFeasible(offer, params);
      const qtyBad = !isQuantityFeasible(offer, params);
      if (priceBad && qtyBad) return EVAL.NOT_PROFITABLE_ON_BOTH;
      if (priceBad) return EVAL.NOT_PROFITABLE_ON_PRICE;
      if (qtyBad) return EVAL.NOT_PROFITABLE_ON_QUANTITY;
      return EVAL.NOT_PROFITABLE_ON_BOTH;
    }
    if (offer.price == null && qtyInRange(offer.quantity)) {
      return isQuantityFeasible(offer, params) ? EVAL.OFFER_QUANTITY
                                               : EVAL.NOT_PROFITABLE_ON_BOTH;
    }
    if (offer.quantity == null && priceInRange(offer.price)) {
      return isPriceFeasible(offer, params) ? EVAL.OFFER_PRICE
                                            : EVAL.NOT_PROFITABLE_ON_BOTH;
    }
    if (offer.price != null && !priceInRange(offer.price)) return EVAL.INVALID_OFFER;
    if (offer.quantity != null && !qtyInRange(offer.quantity)) return EVAL.INVALID_OFFER;
    return EVAL.NOT_OFFER;
  }

  /* ── Counter-offer solvers (optimal.py) ───────────────────────────── */
  function optimalWholesalePriceForQuantity(offer) {
    const q = offer.quantity;
    const pm = Math.max(CONSTRAINT_BOT, CONSTRAINT_USER);
    const es = expectedDemand(q);
    const target = nashBargainingSolution(CONSTRAINT_USER, CONSTRAINT_BOT).profit;
    const bestP = Math.floor((pm - target / es) * 100) / 100;
    return bestP > 0 ? [bestP, q] : [null, null];
  }

  function optimalQuantityForWholesalePrice(offer) {
    const p = offer.price;
    const pm = Math.max(CONSTRAINT_BOT, CONSTRAINT_USER);
    const c = Math.min(CONSTRAINT_BOT, CONSTRAINT_USER);
    const target = nashBargainingSolution(CONSTRAINT_USER, CONSTRAINT_BOT).profit;
    const es = expectedDemand;
    const retailerProfit = (q) => (pm - p) * es(q);
    const userProfit = (q) => p * es(q) - c * q;

    function retailerRoots(b) {
      const a = pm - p;
      const rad = -a * (D_MAX - D_MIN) * (2 * b - pm * (D_MAX + D_MIN) + p * (D_MAX + D_MIN));
      if (rad < -1e-12) return null;
      const s = Math.sqrt(Math.max(0, rad));
      return [(D_MAX * a - s) / a, (D_MAX * a + s) / a];
    }

    const cand = new Set([D_MIN, D_MAX]);
    const roots = retailerRoots(target);
    if (roots) {
      for (let q of roots) {
        q = Math.max(Math.min(q, D_MAX), D_MIN);
        const b = Math.floor(q);
        for (const k of [-2, -1, 0, 1, 2]) {
          const qi = b + k;
          if (qi >= D_MIN && qi <= D_MAX) cand.add(qi);
        }
      }
    }
    { // retailer vertex = D_MAX
      const b = Math.floor(D_MAX);
      for (const k of [-3, -2, -1, 0, 1, 2, 3]) {
        const qi = b + k;
        if (qi >= D_MIN && qi <= D_MAX) cand.add(qi);
      }
    }
    const feas = [...cand].filter((q) => retailerProfit(q) + 1e-9 >= target);
    if (!feas.length) return [null, null];
    const bestQ = feas.reduce((a, b) => (userProfit(b) > userProfit(a) ? b : a));
    return [Math.round(p * 100) / 100, bestQ];
  }

  function optimalCounterOffer(evaluation, offer) {
    let price = null, quantity = null;
    if (evaluation === EVAL.ACCEPT || evaluation === EVAL.INVALID_OFFER) {
      return [null, null];
    } else if (evaluation === EVAL.OFFER_PRICE ||
               evaluation === EVAL.NOT_PROFITABLE_ON_QUANTITY) {
      [price, quantity] = optimalQuantityForWholesalePrice(offer);
    } else if (evaluation === EVAL.OFFER_QUANTITY ||
               evaluation === EVAL.NOT_PROFITABLE_ON_PRICE) {
      [price, quantity] = optimalWholesalePriceForQuantity(offer);
    } else {
      [price, quantity] = nashBargainingSolution(CONSTRAINT_USER, CONSTRAINT_BOT).offer;
    }
    if (price == null || quantity == null) {
      [price, quantity] = nashBargainingSolution(CONSTRAINT_USER, CONSTRAINT_BOT).offer;
    }
    return [price, quantity];
  }

  /* ── Scripted messages (bot_messages.py) ──────────────────────────── */
  const offerString = (p, q) =>
    `a Wholesale Price of ${p.toFixed(2)}€ and a quantity of ${q} units`;

  const FIRST_MESSAGE =
    "Hi Supplier! I'm excited to start our negotiation. As we begin, I'd " +
    "like to give you the opportunity to make an offer first. Or if you " +
    "prefer, I can make the first offer. Just let me know!";
  const ACCEPT_FROM_INTERFACE =
    "That works for me — I accept your offer of %s. Thank you, finalizing the deal now.";
  const ACCEPT_FROM_CHAT =
    "That works for me — I accept %s. I have put it in the interface as a " +
    "binding offer: please click the CONFIRM button to finalize the deal.";
  const RESPONSES = {
    [EVAL.NOT_PROFITABLE_ON_BOTH]:
      "I'm afraid those terms don't work for me. I could agree to %s — I have put that offer in the interface.",
    [EVAL.NOT_PROFITABLE_ON_PRICE]:
      "That wholesale price is too high for me. How about %s? I have put that offer in the interface.",
    [EVAL.NOT_PROFITABLE_ON_QUANTITY]:
      "That quantity doesn't work for me at this price. How about %s? I have put that offer in the interface.",
    [EVAL.OFFER_PRICE]:
      "Thanks — with that wholesale price I could do %s. I have put that offer in the interface.",
    [EVAL.OFFER_QUANTITY]:
      "Thanks — for that quantity I could do %s. I have put that offer in the interface.",
    [EVAL.NOT_OFFER]:
      "I could not read a complete offer from that. Please give me both a " +
      "wholesale price and a quantity (or use the interface below). For reference, %s would work for me.",
    [EVAL.INVALID_OFFER]:
      `Those terms are outside the allowed ranges (price ${PRICE_MIN}-${PRICE_MAX}€, ` +
      `quantity ${QTY_MIN}-${QTY_MAX}). Could you adjust your offer? For reference, %s would work for me.`,
  };

  function scriptedResponse(evaluation, cp, cq) {
    const template = RESPONSES[evaluation];
    if (cp == null || cq == null) return template.replace(" %s", " different terms");
    return template.replace("%s", offerString(cp, cq));
  }

  /* ── Chat-offer reader (bot_messages.py, regex port) ──────────────── */
  const NUMBER_PATTERN = /\d+(?:[.,]\d+)?/g;
  function interpretOfferFromChat(message) {
    const numbers = (message.match(NUMBER_PATTERN) || [])
      .map((n) => parseFloat(n.replace(",", ".")));
    let price = null, quantity = null;
    if (numbers.length === 2) {
      price = Math.round(numbers[0] * 100) / 100;
      quantity = Math.trunc(numbers[1]);
    } else if (numbers.length === 3) {
      price = Math.round(numbers[0] * 100) / 100;
      quantity = Math.trunc(numbers[2]);
    }
    return makeOffer(1, price, quantity, true);
  }

  /* ── LLM layer (OpenRouter; falls back to scripted) ───────────────── */
  const getKey = () => localStorage.getItem(KEY_STORAGE) || "";
  const getModel = () => localStorage.getItem(MODEL_STORAGE) || DEFAULT_CHAT_MODEL;

  async function openrouter(model, messages, maxTokens) {
    const controller = new AbortController();
    const timer = setTimeout(() => controller.abort(), 25000);
    try {
      const res = await fetch(OPENROUTER_URL, {
        method: "POST",
        signal: controller.signal,
        headers: {
          Authorization: "Bearer " + getKey(),
          "Content-Type": "application/json",
        },
        body: JSON.stringify({ model, messages, max_tokens: maxTokens || 220 }),
      });
      if (!res.ok) throw new Error("OpenRouter " + res.status);
      const data = await res.json();
      return (data.choices?.[0]?.message?.content || "").trim();
    } finally {
      clearTimeout(timer);
    }
  }

  // Guardrail: NVIDIA content-safety model on the visitor's message.
  async function moderateInput(text) {
    if (!getKey()) return { safe: true };
    try {
      const verdict = await openrouter(SAFETY_MODEL, [
        { role: "user", content: text },
      ], 80);
      return { safe: !/unsafe/i.test(verdict), verdict };
    } catch {
      return { safe: true }; // guardrail unavailable → don't block the demo
    }
  }

  // Phrase the rule-based decision with the chat LLM; validate that the
  // exact counter terms survived generation, else fall back to script.
  async function llmPhrase(intentDescription, mustContain, scripted, history) {
    if (!getKey()) return scripted;
    try {
      const sys =
        "You are the Retailer in a supply-chain wholesale negotiation demo " +
        "against a human Supplier. Production cost (1.00€) is common " +
        "knowledge; your retail-price information is private — never reveal " +
        "your margins or reasoning thresholds. A rule-based engine has " +
        "already made your decision; your only job is to phrase it " +
        "naturally, in a warm but businesslike tone, in at most 50 words, " +
        "plain text only. Decision to convey: " + intentDescription +
        (mustContain ? " You MUST state these exact terms verbatim: " + mustContain + "." : "");
      const messages = [{ role: "system", content: sys }]
        .concat(history.slice(-8))
        .concat([{ role: "user", content: "Write your reply now." }]);
      const out = await openrouter(getModel(), messages);
      if (!out) return scripted;
      if (mustContain && !out.includes(mustContain)) return scripted;
      return out;
    } catch {
      return scripted;
    }
  }

  /* ── UI wiring ─────────────────────────────────────────────────────── */
  const $ = (id) => document.getElementById(id);
  const chatOutput = $("chat-output");
  const chatInput = $("chat-input");
  const history = []; // OpenAI-format transcript for LLM context
  let offers = [];
  let finished = false;
  let busy = false;

  const esc = (s) =>
    s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");

  function addMessage(nick, body) {
    const div = document.createElement("div");
    div.className = "neg-msg" + (nick === "Retailer (AI)" ? " neg-msg--bot" : "");
    div.innerHTML =
      `<span class="neg-msg__nick">${nick}</span>` +
      `<span class="neg-msg__body">${esc(body)}</span>`;
    chatOutput.appendChild(div);
    chatOutput.scrollTop = chatOutput.scrollHeight;
  }
  function botSays(text) {
    addMessage("Retailer (AI)", text);
    history.push({ role: "assistant", content: text });
  }
  function setTyping(on) {
    $("typing").style.visibility = on ? "visible" : "hidden";
  }
  const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

  function lastBotOffer() {
    const bot = offers.filter((o) => o.idx === BOT_ID);
    return bot.length ? bot[bot.length - 1] : null;
  }
  function refreshInterface() {
    const bot = lastBotOffer();
    $("otherProposal").innerHTML = bot
      ? `€ ${bot.price.toFixed(2)}<br>${bot.quantity}`
      : "(none)<br>&nbsp;";
    $("btnAccept").disabled = !bot || finished;
    const mine = offers.filter((o) => o.idx !== BOT_ID && !o.fromChat);
    $("myProposal").innerHTML = mine.length
      ? `€ ${mine[mine.length - 1].price.toFixed(2)}<br>${mine[mine.length - 1].quantity}`
      : "(none)<br>&nbsp;";
  }

  function finishDeal(price, quantity) {
    finished = true;
    refreshInterface();
    chatInput.disabled = true;
    $("btn-chat").disabled = true;
    $("btn-offer").disabled = true;
    $("btnAccept").disabled = true;
    const sp = profitSupplier(price, quantity, PRODUCTION_COST);
    $("deal-terms").textContent =
      `Deal: wholesale price ${price.toFixed(2)}€ · quantity ${quantity} units`;
    $("deal-profit").textContent =
      `Your expected profit as Supplier: ${sp.toFixed(2)} ` +
      `(wholesale revenue on expected sales minus production cost of 1.00€/unit).`;
    $("deal-panel").hidden = false;
    $("deal-panel").scrollIntoView({ behavior: "smooth", block: "center" });
  }

  async function respondTo(offer) {
    const evaluation = evaluate(offer);
    setTyping(true);
    await sleep(BOT_RESPONSE_DELAY);

    if (evaluation === EVAL.ACCEPT) {
      const terms = offerString(offer.price, offer.quantity);
      if (offer.fromChat) {
        const scripted = ACCEPT_FROM_CHAT.replace("%s", terms);
        const text = await llmPhrase(
          "ACCEPT the Supplier's chat offer of " + terms +
          "; tell them you have placed it in the interface as a binding " +
          "offer and they must click CONFIRM to finalize.",
          null, scripted, history);
        setTyping(false);
        botSays(text);
        await sleep(BOT_ACCEPT_DELAY);
        offers.push(makeOffer(BOT_ID, offer.price, offer.quantity, false));
        refreshInterface();
      } else {
        const scripted = ACCEPT_FROM_INTERFACE.replace("%s", terms);
        const text = await llmPhrase(
          "ACCEPT the Supplier's binding offer of " + terms +
          " and announce you are finalizing the deal.",
          null, scripted, history);
        setTyping(false);
        botSays(text);
        await sleep(BOT_ACCEPT_DELAY);
        finishDeal(offer.price, offer.quantity);
      }
      return;
    }

    const [cp, cq] = optimalCounterOffer(evaluation, offer);
    const scripted = scriptedResponse(evaluation, cp, cq);
    const nonOffer = evaluation === EVAL.NOT_OFFER || evaluation === EVAL.INVALID_OFFER;
    const terms = cp != null && cq != null ? offerString(cp, cq) : null;
    const intent = {
      [EVAL.NOT_PROFITABLE_ON_BOTH]:
        "REJECT their terms (both price and quantity unworkable) and counter with ",
      [EVAL.NOT_PROFITABLE_ON_PRICE]:
        "REJECT because the wholesale price is too high for you; counter with ",
      [EVAL.NOT_PROFITABLE_ON_QUANTITY]:
        "REJECT because that quantity does not work at that price; counter with ",
      [EVAL.OFFER_PRICE]:
        "They only named a price; propose completing it as ",
      [EVAL.OFFER_QUANTITY]:
        "They only named a quantity; propose completing it as ",
      [EVAL.NOT_OFFER]:
        "You could not read a complete offer from their message; ask for both " +
        "a wholesale price and a quantity, mentioning as reference ",
      [EVAL.INVALID_OFFER]:
        `Their terms are outside the allowed ranges (price ${PRICE_MIN}-${PRICE_MAX}€, ` +
        `quantity ${QTY_MIN}-${QTY_MAX}); ask them to adjust, mentioning as reference `,
    }[evaluation] + (terms || "different terms") +
      (nonOffer ? "." : ". Mention that you have placed this offer in the interface.");

    const text = await llmPhrase(intent, terms, scripted, history);
    setTyping(false);
    botSays(text);

    if (!nonOffer && cp != null && cq != null) {
      offers.push(makeOffer(BOT_ID, cp, cq, false));
      refreshInterface();
    }
  }

  /* ── Event handlers ────────────────────────────────────────────────── */
  window.sendMessage = async function () {
    if (busy || finished) return;
    const body = chatInput.value.trim();
    if (!body) return;
    busy = true;
    chatInput.value = "";
    addMessage("You (Supplier)", body);
    history.push({ role: "user", content: body });

    const mod = await moderateInput(body);
    if (!mod.safe) {
      setTyping(false);
      botSays("Let's keep this professional and focused on the negotiation, " +
              "please. I'm happy to continue when you have an offer to discuss.");
      busy = false;
      return;
    }
    const offer = interpretOfferFromChat(body);
    await respondTo(offer);
    busy = false;
  };

  window.sendOffer = async function () {
    if (busy || finished) return;
    const price = parseFloat($("myPrice").value);
    const quantity = parseInt($("myQuantity").value, 10);
    if (isNaN(price) || isNaN(quantity)) {
      addMessage("System", "Enter both a price and a quantity first.");
      return;
    }
    busy = true;
    const offer = makeOffer(1, Math.round(price * 100) / 100, quantity, false);
    offers.push(offer);
    refreshInterface();
    addMessage("You (Supplier)",
      `[binding offer] ${offer.price.toFixed(2)}€ · ${offer.quantity} units`);
    history.push({ role: "user",
      content: `(binding interface offer) price ${offer.price}, quantity ${offer.quantity}` });
    await respondTo(offer);
    busy = false;
  };

  window.sendAccept = function () {
    if (busy || finished) return;
    const bot = lastBotOffer();
    if (!bot) return;
    addMessage("You (Supplier)",
      `[confirmed] ${bot.price.toFixed(2)}€ · ${bot.quantity} units`);
    finishDeal(bot.price, bot.quantity);
  };

  window.resetDemo = function () {
    offers = [];
    history.length = 0;
    finished = false;
    busy = false;
    chatOutput.innerHTML = "";
    chatInput.disabled = false;
    chatInput.value = "";
    $("btn-chat").disabled = false;
    $("btn-offer").disabled = false;
    $("myPrice").value = "";
    $("myQuantity").value = "";
    $("deal-panel").hidden = true;
    refreshInterface();
    greet();
  };

  /* ── Settings panel ───────────────────────────────────────────────── */
  function refreshLlmStatus() {
    const el = $("llm-status");
    if (getKey()) {
      el.textContent = `LLM chat active: ${getModel()} + ${SAFETY_MODEL} guardrail (via OpenRouter).`;
      el.className = "neg-status neg-status--on";
    } else {
      el.textContent =
        "Running with the experiment's scripted messages (no API key set). " +
        "Add a free OpenRouter key under Settings to enable LLM-generated chat.";
      el.className = "neg-status";
    }
  }
  window.saveSettings = function () {
    const key = $("api-key").value.trim();
    const model = $("chat-model").value.trim();
    if (key) localStorage.setItem(KEY_STORAGE, key);
    else localStorage.removeItem(KEY_STORAGE);
    if (model && model !== DEFAULT_CHAT_MODEL) localStorage.setItem(MODEL_STORAGE, model);
    else localStorage.removeItem(MODEL_STORAGE);
    refreshLlmStatus();
    $("settings-saved").hidden = false;
    setTimeout(() => { $("settings-saved").hidden = true; }, 2000);
  };

  /* ── Boot ─────────────────────────────────────────────────────────── */
  async function greet() {
    setTyping(true);
    await sleep(800);
    setTyping(false);
    botSays(FIRST_MESSAGE);
  }

  document.addEventListener("DOMContentLoaded", function () {
    chatInput.addEventListener("keydown", function (e) {
      if (e.key === "Enter") window.sendMessage();
    });
    $("api-key").value = getKey();
    $("chat-model").value = getModel();
    refreshLlmStatus();
    refreshInterface();
    greet();
  });
})();
